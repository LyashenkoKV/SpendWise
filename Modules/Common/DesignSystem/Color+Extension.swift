import SwiftUI

extension Color {

    // App
    static let accentColor = Color("accentColor")
    static let subColor = Color("subColor")

    // Background
    static let bgPrimary = Color("backgroundPrimary")
    static let bgSecondary = Color("backgroundSecondary")

    // Category
    static let cgOrange = Color("orange-cg")
    static let cgYellow = Color("yellow-cg")

    // Text
    static let accentText = Color("accentText")
    static let hintText = Color("hintText")
    static let systemText = Color("systemText")
    static let primaryText = Color("primaryText")
    static let secondaryText = Color("secondaryText")
}
