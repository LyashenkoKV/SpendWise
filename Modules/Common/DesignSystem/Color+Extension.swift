import SwiftUI

extension Color {

    // App
    public static let accentAppColor = Color("accentColor")
    public static let subAppColor = Color("subColor")

    // Background
    public static let bgPrimary = Color("backgroundPrimary")
    public static let bgSecondary = Color("backgroundSecondary")

    // Category
    public static let cgOrange = Color("orange-cg")
    public static let cgYellow = Color("yellow-cg")

    // Text
    public static let accentText = Color("accentText")
    public static let hintText = Color("hintText")
    public static let systemText = Color("systemText")
    public static let primaryText = Color("primaryText")
    public static let secondaryText = Color("secondaryText")
}
